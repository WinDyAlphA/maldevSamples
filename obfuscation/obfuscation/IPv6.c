char* GenerateIpv6(int a, int b, int c, int d,
    int e, int f, int g, int h,
    int i, int j, int k, int l,
    int m, int n, int o, int p)
{
    /* format: XXXX:XXXX:XXXX:XXXX où chaque XXXX = 4 octets en hex (2+2) */
    /* taille max estimée : 4*(4*2)+3(:)+1 = 36 safe => on prend 64 */
    char* result = (char*)malloc(64);
    if (!result) return NULL;
    /* on compose directement dans result pour éviter buffers temporaires */
    snprintf(result, 64,
        "%02X%02X:%02X%02X:%02X%02X:%02X%02X:%02X%02X:%02X%02X:%02X%02X:%02X%02X",
        a, b, c, d, e, f, g, h, i, j, k, l, m, n, o, p);
    return result;
}

